// Helper για εύρεση άρθρου
function findLawText(articleRef) {
    // Parse the reference like "Άρθρο 1, παρ. 1 Ν. 3284/2004" or "Άρθρο 1A"
    const match = articleRef.match(/Άρθρο\s*(\d+\w*)/i);
    if (!match) return null;
    
    const articleNum = match[1].toLowerCase().replace('α', 'a'); // Handle Greek 'Α' as 'A'
    const articleKey = `article_${articleNum}`;
    
    return LawTexts[articleKey] || null;
}

// Helper για εύρεση συγκεκριμένης παραγράφου
function findParagraph(articleRef) {
    const article = findLawText(articleRef);
    if (!article) return null;
    
    const parMatch = articleRef.match(/παρ\.?\s*(\d+)/i);
    if (parMatch && article.paragraphs) {
        return article.paragraphs[parMatch[1]] || null;
    }
    
    return article;
}

// Helper για πλήρες κείμενο
function getFullArticleText(articleKey) {
    const article = LawTexts[articleKey];
    if (!article) return null;
    
    let text = `${article.title}\n`;
    text += `${article.fek}\n`;
    if (article.lastAmended) {
        text += `Τελευταία τροποποίηση: ${article.lastAmended}\n`;
    }
    text += `\n`;
    
    if (article.paragraphs) {
        for (const [key, para] of Object.entries(article.paragraphs)) {
            if (key !== 'exceptions') {
                text += para.text + '\n\n';
            }
        }
        if (article.paragraphs.exceptions) {
            text += '\nΕξαιρέσεις:\n' + article.paragraphs.exceptions.text + '\n';
        }
    }
    
    return text;
}

